import java.util.Scanner;

public class Ejerciciof02 {

    public static void main(String[] args) {

		

		// Invocando Funcion
		System.out.println("Resultado resta:"+restar(5, 4) );
		System.out.println("Resultado resta:"+restar(2, 3) );
		System.out.println("Resultado resta:"+restar(7, 8) );
		System.out.println("Resultado resta:"+restar(1, 20) );
		System.out.println("Resultado resta:"+restar(78, 20) );
	}

	public static int restar(int a, int b){
		int respuesta = a - b;
		return respuesta;
	}

	
	

}

    

